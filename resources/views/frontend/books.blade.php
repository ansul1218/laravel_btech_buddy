                    function filterTablep1(language, currentTab) {
                        var table = document.getElementById("pbooks-table1");
                        var rows = table.getElementsByTagName("tr");

                        for (var i = 1; i < rows.length; i++) {
                            var row = rows[i];
                            var cells = row.getElementsByTagName("td");
                            var pbookLanguage = cells[1].innerText;

                            var showRow = true;

                            // Filter by language
                            if (language && pbookLanguage.toLowerCase() !== language.toLowerCase()) {
                                showRow = false;
                            }

                            if (showRow) {
                                row.style.display = "";
                            } else {
                                row.style.display = "none";
                            }
                        }

                        // Set active tab
                        var tablinks = document.getElementsByClassName("tablinks");
                        for (var i = 0; i < tablinks.length; i++) {
